#include <iostream>
using namespace std;
int main ()
{
    int a = 10; // Declare and initialize an integer variable a with value 10

    int b = 20; // Declare and initialize an integer variable b with value 20

    int mul = a * b; // Declare an integer variable mul and store the result of a * b

    // Output the multiplication of a and b directly using the expression (a * b)
    cout << "Multiplication of a and b is : " << (a * b) << endl;

    // Output the multiplication of a and b using the mul variable
    cout << "Multiplication of a and b is : " << mul << endl;

    return 0; // Return 0 to indicate successful execution
}