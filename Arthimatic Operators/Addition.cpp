#include <iostream>
using namespace std;

int main ()
{
    int a = 10; // Declare and initialize an integer variable a with value 10

    int b = 20; // Declare and initialize an integer variable b with value 20

    int sum = a + b; // Declare an integer variable sum and store the result of a + b

    // Output the sum of a and b directly using the expression (a + b)
    cout << "Sum of a and b is : " << (a + b) << endl;

    // Output the sum of a and b using the sum variable
    cout << "Sum of a and b is : " << sum << endl;

    return 0; // Return 0 to indicate successful execution
}